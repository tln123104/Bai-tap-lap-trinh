#include <iostream>
using namespace std;

int main()
{
    int sobat, giabat, sodia, giadia;
    cout << "So bat: "; cin >> sobat;
    cout << "Gia bat: "; cin >> giabat;
    cout << "So dia: "; cin >> sodia;
    cout << "Gia dia: "; cin >> giadia;
    cout << "\nSo tien: " << sobat * giabat + sodia * giadia;
    return 0;
}
